  # Write a program called age.rb that asks user how old they are and then tells them how old they will be in 10, 20, 30 and 40 years. 

  p "How old are you?" 
  age = gets.chomp.to_i 
  
  p "In 10 years you will be:" 
  p age + 10 
    # = > age+10 
  
  p "In 20 years you will be:" 
  p age + 20 
    # = > age+20 
    
  p "In 30 years you will be:" 
  p age + 30 
    # = > age+30
  
  p "In 40 years you will be:" 
  p age + 40 
    # = > age+40 


